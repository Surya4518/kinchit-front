  $(document).on('click', '.delete-message', function(event) {
    event.preventDefault();
    var shortdelete = $(this).data('short_delete');
    $.confirm({
      title: 'Confirmation',
      content: 'Are you sure you want to delete this message?',
      type: 'green',
      typeAnimated: true,
      buttons: {
        confirm: {
          text: 'Yes',
          btnClass: 'btn-green',
          action: function() {
            var formdata = new FormData();
            formdata.append('token', getCookie('token'));
            formdata.append('device_id', '1');
            formdata.append('short_delete', shortdelete);

            $.ajax({
              type: 'POST',
              url: "{{ env('API_URL') }}delete-shortlist",
              data: formdata,
              processData: false,
              contentType: false,
              success: function(response) {
                if (response.status === 200) {
                  $.confirm({
                    title: 'Success!',
                    content: response['message'],
                    type: 'success',
                    typeAnimated: true,
                    closeIcon: true,
                    buttons: {
                      ok: {
                        text: 'OK',
                        btnClass: 'btn-success',
                        action: function() {
                          location.reload();
                        }
                      },
                    }
                  });
                } else {
                  $.confirm({
                    title: 'Error!',
                    content: 'Failed to delete message.',
                    type: 'red',
                    typeAnimated: true,
                    buttons: {
                      ok: {
                        text: 'OK',
                        btnClass: 'btn-red',
                        action: function() {}
                      }
                    }
                  });
                }
              },
              error: function(xhr, status, error) {
                alert('An error occurred while deleting the message.');
              }
            });
          }
        },
        cancel: {
          text: 'No',
          btnClass: 'btn-red',
          action: function() {}
        }
      }
    });
  });

