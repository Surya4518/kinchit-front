    function coursesList() {
        var formdata = new FormData();
        formdata.append('token', getCookie('token'));
        formdata.append('device_id', '1');

        $.ajax({
            type: 'post',
            url: "{{ env('API_URL') }}get-course-for-user",
            data: formdata,
            processData: false,
            contentType: false,
            success: function(response) {
                if (response['status'] == 200) {
                    var data = response['data'];
                    
                    if(data.length > 0){
                        
                    var html = '';
                    data.forEach(function(post) {
                        if (post.guid != null) {
                            html += `
        <div class="col-md-4 mb-5">
            <a href="course/${post.post_name}">
                <div class="card">
                    <div class="card-details">
                        <h2 class="text-title">${post.post_title}</h2>
                        <img src="http://kinchit-admin.senthil.in.net/public/${post.guid}" alt="${post.post_title}">
                    </div>
                    <a href="course/${post.post_name}" class="card-button">${('Kalakshepam ' + post.status).toUpperCase()}</a>
                </div>
            </a>
        </div>`;
                        } else {
                            html += `
        <div class="col-md-4 mb-5">
            <a href="course/${post.post_name}">
                <div class="card">
                    <div class="card-details">
                        <h2 class="text-title">${post.post_title}</h2>
                        <img src="{{ env('MAIN_URL') }}public/assets/images/kalakshepam/nammazhwar.jpg" class="img-fluid kalak-img">
                    </div>
                    <a href="course/${post.post_name}" class="card-button">${('Kalakshepam ' + post.status).toUpperCase()}</a>
                </div>
            </a>
        </div>`;
                        }
                    });
                    $('.course-detail').html(html);
                    }else{
                        $('.course-detail').html('<div class="row"><div class="col-12 text-center"><h4>Data Not Found..!</h4></div></div>');
