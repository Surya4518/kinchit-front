            var data = response['data'];
            var threadsByUser = groupThreadsByUser(data);
            var html = '';
            var startIdx = (page - 1) * 10; // Calculate the starting index for the threads
            var endIdx = startIdx + 10; // Calculate the ending index for the threads
            var count = 0; // Track the number of threads
            for (var userId in threadsByUser) {
              if (threadsByUser.hasOwnProperty(userId)) {
                var userThreads = threadsByUser[userId];
                var userDisplayName = userThreads[0]['user'][0]['display_name'];
                var userType = userThreads[0]['user'][0]['user_type'];
                var userImage = userThreads[0]['userdetails'][0] ? userThreads[0]['userdetails'][0]['user_image'] : null;
                userThreads.forEach(function(thread) {
                  if (count >= startIdx && count < endIdx) { // Show only 10 threads for the current page
                    var created_at = new Date(thread['created_at']);
                    var now = new Date();
                    var diffMs = now - created_at;
                    var diffHours = Math.round(diffMs / (1000 * 60 * 60));
                    var imageSrc = userImage ? userImage : "{{ env('MAIN_URL') }}public/assets/images/audio/dhm2.jpg";

                    var diffText;
                    if (diffHours < 1) {
                      diffText = 'Less than an hour ago';
                    } else if (diffHours < 24) {
                      diffText = 'Updated ' + diffHours + ' hours ago';
                    } else if (diffHours < 24 * 30) {
                      var days = Math.floor(diffHours / 24);
                      diffText = 'Updated ' + days + ' day' + (days > 1 ? 's' : '') + ' ago';
                    } else if (diffHours < 24 * 365) {
                      var months = Math.floor(diffHours / (24 * 30));
                      diffText = 'Updated ' + months + ' month' + (months > 1 ? 's' : '') + ' ago';
                    } else {
                      var years = Math.floor(diffHours / (24 * 365));
                      diffText = 'Updated ' + years + ' year' + (years > 1 ? 's' : '') + ' ago';
                    }
                    html += `
                                    <div class="d-flex mb-4 pro">
                                        <div class="col-2 mr-1">
                                            <div class="orthodepart-icon">
                                                <a href="/rply/${thread.post_name}"><img src="${imageSrc}" width="100%"></a>
                                            </div>
                                        </div>
                                        <div class="col-10">
                                            <a href="">
                                                <h3 class="orthodepart-sub-head">${thread.post_title}</h3>
                                            </a>
                                            <div class="orthodepart-para mb-3">
                                                <span class="times-create">${diffText}</span>
                                                <span class="type-category"><a href=""><span class="flag"><img src="{{ env('MAIN_URL') }}public/assets/images/audio/rectangle.webp"></span>${userType}</a></span>
                                                <span class="rply"><a href="#">Last Reply</a></span>
                                                <span class="author">${userDisplayName}</span>
                                            </div>
                                        </div>
                                    </div>`;
                  }
                  count++;
                });
              }
            }
            $('.threads-contents').html(html);

            function generatePaginationHtml(page, totalPages) {
              var paginationHtml = '<ul class="d-flex">';
              if (page > 1) {
                paginationHtml += `<li style="list-style-type:none;"><a href="#" class="previous round" data-page="${page - 1}">Previous</a></li>`;
              }
              var startPage = Math.max(1, page - 2);
              var endPage = Math.min(totalPages, startPage + 4);

              if (startPage > 1) {
                paginationHtml += `<li style="list-style-type:none;"><a href="#" class="page" data-page="1">1</a></li>`;
                if (startPage > 2) {
                  paginationHtml += '<li style="list-style-type:none;">...</li>';
                }
              }

              for (var i = startPage; i <= endPage; i++) {
                if (i === page) {
                  paginationHtml += `<li style="list-style-type:none;"><span class="current">${i}</span></li>`;
                } else {
                  paginationHtml += `<li style="list-style-type:none;"><a href="#" class="page" data-page="${i}">${i}</a></li>`;
                }
              }

              if (endPage < totalPages) {
                if (endPage < totalPages - 1) {
                  paginationHtml += '<li style="list-style-type:none;">...</li>';
                }
                paginationHtml += `<li style="list-style-type:none;"><a href="#" class="page" data-page="${totalPages}">${totalPages}</a></li>`;
              }

              if (page < totalPages) {
                paginationHtml += `<li style="list-style-type:none;"><a href="#" class="next round" data-page="${page + 1}">Next</a></li>`;
              }

              paginationHtml += '</ul>';

              return paginationHtml;
            }

            var totalPages = 10; // Total number of pages (example value)
            var currentPage = page; // Current page
            var paginationHtml = generatePaginationHtml(currentPage, totalPages);
            $('.pagination').html(paginationHtml);

            // Pagination click event handler
            $('.pagination').on('click', '.page', function(e) {
              e.preventDefault();
              var pageNumber = parseInt($(this).attr('data-page'));
