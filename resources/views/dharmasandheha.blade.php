    function groupThreadsByUser(data) {
      var threadsByUser = {};
      data.forEach(function(thread) {
        var userId = thread['user'][0]['ID'];
        if (!threadsByUser[userId]) {
          threadsByUser[userId] = [];
        }
        threadsByUser[userId].push(thread);
      });
      return threadsByUser;
    }
